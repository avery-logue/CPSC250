/**
 * This code prints out all of the command line arguments
 * @author Avery's PC
 * @version 1/11/17
 */
public class Arguments {
    /**
     * This runs through args[] and prints out each argument
     * @param args command line arguments
     */
    public static void main(String args[]) {
	    for(String arg: args)
            System.out.println(arg);
    }
}
