/**
  Test if number is odd
  @author Avery Logue
  @version 1/16/2018
 */
public class Odd {

    /** Return true if a number is odd, otherwise return false
    // Web-cat will say this code has some stylistic issues, but
    // more importantly it might have a logical problem.
    // Let's test to be sure it is correct.
      @param num integer to test
      @return boolean true if number is odd; false otherwise
     */
    public static boolean isOdd(int num) {
        return (num % 2 != 0);
    }

}
