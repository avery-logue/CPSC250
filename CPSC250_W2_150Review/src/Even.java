/**
  Test if number is even
  @author Avery Logue
  @version 1/23/2018
 */
public class Even {

    /** Return true if a number is even, otherwise return false
    // Web-cat will say this code has some stylistic issues, but
    // more importantly it might have a logical problem.
    // Let's test to be sure it is correct.
      @param num integer to test
      @return boolean true if number is even; false otherwise
     */
    public static boolean isEven(int num) {
        return (num % 2 == 0);
    }

}
